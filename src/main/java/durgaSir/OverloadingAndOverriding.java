package durgaSir;

public class OverloadingAndOverriding {
	
	/**
	 * **********Overloading***********(Not required extends concept)
	 * If we have same name method in a class with different arguments its call as overloading 
	 * method name must be same 
	 * arguments order must be different (integer ) (integer,string).
	 * method signature must be different 
	 * private , static & final can be overloaded 
	 * Also knows as compile time polymorsphism  , early binding and static binding 
	 * access modifier no restriction 
	 * Note:We have to check the method name and arguments only 
	 * 
	 * ***********Overriding**********************(required extends concept)
	 * Method Overriding in Java occurs when a subclass provides its own implementation of a method already defined in its parent class. 
	 * The overridden method in the child class must have the same name, return type, and parameters as the method in the parent class
	 *  method name must be same 
	 * arguments order must be same
	 *  method signature must be same  
	 * private , static & final can not be overloaded 
	 * Also knows as run time polymorsphism , dynamic binding and late binding 
	 * we can reduce the scope of modifier but can increase the scope there 
	 *  Note: we have to check the everything check at the time achieve the overriding concept 
	 * 
	 */

}
