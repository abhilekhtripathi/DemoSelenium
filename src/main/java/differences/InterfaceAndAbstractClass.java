package differences;

public class InterfaceAndAbstractClass {
	
	/**
	 * ***************Interface**************************
	 * if we don't know the requirement then go for the interface 
	 * 
	 * In interface every methods are Public and abstract so that we can achieve 100% abstraction in interface 
	 * 
	 * We can not declare method modifiers are private and protected 
	 * 
	 * Every variable inside the Interface is Public , Static and Final , whether we declare or not 
	 * 
	 * For interface can perform the multiple inheritance means implements multiple class's
	 * 
	 * for the interface we can not declare the constructors 
	 * 
	 * inside the interface we can not declare the instance variable and static block otherwise will be receive the compile time error 
	 * 
	 * 
	
	 * ***************Abstract Class***************
	 * If we know the partial requirement then go for the abstract class
	 * 
	 * in abstract class we have abstract method and concrete methods also (concrete means - full implementation there)
	 * 
	 * there is not restriction to made any methods 
	 * 
	 * No need to make it Public, Static & Final for the abstract class
	 * 
	 * for the abstract class can perform the single inheritance using extends keyword 
	 * 
	 * for the interface we can  declare the constructors at the time of child object creation 
	 * 
	 * there is not restriction can declare the the instance variable and static blocks 
	 * 
	 * 
	 * 
	 * 
	 */

}
