package collection_differences;

public class ArraysAndCollections {
	/**
	 * **********Arrays*******************
	 * 
	 * Arrays can store the primitive & non primitive data 
	 * 
	 * int [] a= {10,20,30};
	 * 
	 * Test{
	 * 
	 * Test[] t = {obj1,obj2,obj3};
	 * 
	 * }
	 * 
	 * 
	 * Array can store only homogeneous(same) types of data
	 * 
	 * Arrays size is fixed and we can not increased at the run time arrays size
	 * 
	 * Array are inbuilt feature in JAVA and we have to develop the algorithm own 
	 * 
	 * 
	 * 
	 * **********Collections***************
	 * Collections can contains only non primitives type of data 
	 * 
	 * ArrayList al = new ArrayList(){
	 *    al.add(10);
	 *    al.add(Obj1);
	 * }
	 * 
	 * We can store the heterogeneous(different) types of the data there
	 * 
	 * We can increased or decreased at run collection array and it's not fixed 
	 * 
	 * Collections is API interface there provide predefined methods function and predefined class's 
	 * 
	 * 
	 */
	
	

}
